    G,_=get_Gravity_Matrix(np.array([st1,0,0,st2,0,0]),alpha = alpha,additional_mass=ObjectMass)
    M = M1(st2,ObjectMass)
    acc = np.linalg.solve(M,TAUINIT-G)
